<h2 id="iwouldliketoputonashowwhatnext">I would like to put on a show - what next?</h2>

<p>Do you direct, and have a play or musical you would like to put on? Have you found a great play, but are looking for a director? Have you got your own script tucked away in a drawer waiting to see the light of day? Or have you come up with an experimental performance piece that needs a fitting forum?</p>

<p>Then please do not hesitate to get in touch with us on info@ctcircle.dk!</p>

<p>We are constantly on the lookout for new directors and shows to fill our season and activate our members, and are open to new suggestions for both shorter and longer projects.</p>

<h2 id="whatkindofshowcanidirect">What kind of show can I direct?</h2>

<p>Basically, you can propose to direct anything you are motivated to put on!</p>

<p>Shows can be vaguely grouped into three categories:</p>

<ul>
<li>large <strong>"main stage" productions</strong>, which typically run for two weeks, have a large budget (between 80,000 and 130,000 kr) and play to an audience of up to 1,000 people at a properly equipped theatre.</li>

<li>smaller <strong>"Fringe" productions</strong>, which typically run for a few evenings or up to a week, have a reduced budget (typically under 20'000 kr), and play to an audience of up to 400 people at a smaller theatre or alternative venue.</li>

<li><strong>Fringe Festival</strong> shows, which are up to 30 minutes long and are performed once or twice at our annual Fringe Festival in May to an audience of ca. 100 people per night.</li>
</ul>

<p>Naturally, the greater the scale of the show, the more experience we ask you to have as a director, so if you are new to directing, a Fringe Festival contribution or a small Fringe production would be a great place to start.</p>

<h2 id="whatdoesittaketodirectashowforthectc">What does it take to direct a show for the CTC?</h2>

<p>If directing a show sounds interesting, but you are hesitating because you don't know what it involves and how much is expected of you, then read on.</p>

<p>As the director of a show, you will be responsible for the entire creative process to get it from script to stage. To let you focus on the creative aspects, we insist that every production has its own production manager and assistant director. The assistant director (AD) helps schedule rehearsals, coordinate logistics with the actors and take notes during the rehearsals. The production manager (PM) is in charge of the budget and putting together the rest of the production team, and is the person responsible for catching all loose ends.</p>

<p>With the AD and PM in place, you can concentrate on your vision for the play, which can be divided into two broad categories: finding and guiding the actors, and creating a concept for the scenography.</p>

<h2 id="auditions">Auditions</h2>

<p>The work with the actors begins with the auditions, where you try to find the suitable people for the various roles in the play. </p> 

<p>Given the fact that we are a community theatre group, we insist that you hold open auditions and don't promise parts to anyone before the auditions have been held. This ensures a level playing field for everyone in our community.</p>

<p>We usually send out electronic forms where people can indicate their interest to audition, and typically have a high response rate, and several audition sessions are usually necessary to cover the level of interest.</p>

<p>You are free to structure the auditions any way you like - some directors ask actors to prepare a monologue (either a specific one or one chosen by them), others ask them to cold read on the day (and some do both). Auditions can also have the form of workshops or rehearsals, where directors work with several actors at once to see how they respond to directions and work together.</p>

<p>Auditions can also be held in two rounds with so-called callbacks, where a selection from the first round are "called back" to audition again.</p>

<h2 id="rehearsals">Rehearsals</h2>

<p>Once you have selected your actors, you can start rehearsing the play with them.</p>

<p>Rehearsals usually take place 2 to 3 times a week (for 2 to 3 hours each), and can run from anywhere between 3 weeks and 6 months, depending on the scale of the production.</p>

<p>During the rehearsals, you will typically want to:</p>

<ul>
<li>do warm-ups and theatre games to help form a sense of cohesion and physical alertness</li>

<li>go through the "blocking" of a show (i.e. figure out where the actors should be moving and standing at various points in a scene - this can be either prepared in advance or established during the rehearsal)</li>

<li>try to establish the mood, emotions, timbre, energy and whatever other magic it takes to bring the show to life</li>
</ul>

<h2 id="scenography">Scenography</h2>

<p>Apart from directing the actors, you will also be ultimately responsible for what the show looks like on stage. You will therefore typically be working together with creative team members, who are responsible for designing each of the following areas:</p>

<ul>
<li>set</li>

<li>costumes</li>

<li>lighting</li>

<li>sound</li>

<li>music</li>

<li>choreography</li>

<li>publicity artwork and texts</li>
</ul>

<p>Your work with these team members will involve discussions about how your overall vision can be implemented, which can be held one-on-one or in production meetings. It is important to involve the Production Manager in these meetings, to make sure that the ideas can be implemented with the available budget, time and resources.</p>

<h2 id="bringyourowncrew">Bring your own crew!</h2>

<p>The CTC has a large database of people who have indicated a willingness to work on various parts of a production, and the committee will help you as best it can to pair you and your production manager with suitable crew members.</p>

<p>However, if you already have experience working with specific crew members, please feel free to bring them into the project.</p>

<p>Also, we can only give a green light for a production to go ahead once a production manager and an assistant director are in place. Again, we can help to pair you with suitable people, but if you can bring your own core crew members into a production, you will exponentially increase your chances of being allowed to do a show!</p>

<h2 id="benice">Be nice!</h2>

<p>Rehearsals can be intense for everyone involved, due to their frequency and self-exposing nature, and we therefore ask our directors to bear in mind that everyone is participating on a voluntary basis and that they therefore treat the actors with respect.</p>

<p>The same goes for crew members, who usually spend hours of their free time doing work for your show with only themselves as company. Again, we emphasise the importance that you are kind to everyone!</p>
